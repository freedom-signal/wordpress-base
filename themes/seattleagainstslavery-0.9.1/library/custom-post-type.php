function register_cpt() {

	$labels = array(
		'name'          => __( 'Resources', 'foundationpress' ),
		'singular_name' => __( 'Resource', 'foundationpress' ),
	);

	$args = array(
		'has_archive' => true,
		'label'       => __( 'Resources', 'foundationpress' ),
		'labels'      => $labels,
		'menu_icon'   => 'dashicons-paperclip',
		'public'      => true,
		'rewrite'     => array( 'slug' => 'resource', 'with_front' => false ),
		'show_ui'     => true,
		'supports'    => array( 'title', 'custom-fields', 'page-attributes' )
	);
	register_post_type( 'resource', $args );

	register_taxonomy( 'type', array( 'resource' ), array(
			'hierarchical'   => true,
			'label'          => 'Type',
			'singular_label' => 'Type',
			'rewrite'        => array(
				'slug'       => 'resource',
				'with_front' => false
			)
		)
	);
	register_taxonomy_for_object_type( 'categories', 'resource' );

	$labels = array(
		'name'          => __( 'Team Members', 'foundationpress' ),
		'singular_name' => __( 'Team Member', 'foundationpress' ),
	);

	$args = array(
		'has_archive' => true,
		'label'       => __( 'Team Members', 'foundationpress' ),
		'labels'      => $labels,
		'menu_icon'   => 'dashicons-admin-users',
		'public'      => true,
		'rewrite'     => array( 'slug' => 'team', 'with_front' => true ),
		'show_ui'     => true,
		'supports'    => array( 'title', 'thumbnail', 'custom-fields', 'page-attributes' )
	);

	register_post_type( 'team', $args );

	register_taxonomy( 'organization', array( 'team' ), array(
			'hierarchical'   => true,
			'label'          => 'Organization',
			'singular_label' => 'Organization',
			'rewrite'        => array(
				'slug'       => 'team',
				'with_front' => false
			)
		)
	);

	register_taxonomy_for_object_type( 'categories', 'team' );

	$labels = array(
		'name'          => __( 'Volunteers', 'foundationpress' ),
		'singular_name' => __( 'Volunteer', 'foundationpress' ),
	);

	$args = array(
		'has_archive' => true,
		'label'       => __( 'Volunteers', 'foundationpress' ),
		'labels'      => $labels,
		'menu_icon'   => 'dashicons-id-alt',
		'public'      => true,
		'rewrite'     => array( 'slug' => 'volunteer', 'with_front' => true ),
		'show_ui'     => true,
		'supports'    => array( 'title', 'custom-fields', 'page-attributes' )
	);

	register_post_type( 'volunteer', $args );
}

